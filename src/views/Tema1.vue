          p.text-small(numero="3" titulo="Conteo y control") Con la especialización laboral y el intercambio, ya los recursos no eran exclusivamente de consumo para la familia, sino que servían para intercambiar por otros, por lo cual se necesitó llevar técnicas de conteo, control y registro, lo que dio origen a la contabilidad como la disciplina que cumple con estos preceptos, y con ella, la escritura en paredes o tablillas, junto con la acumulación de recursos, tal como lo evidencia la historia.
          p.text-small(numero="4" titulo="Negociación") Este ejercicio, con el tiempo, se masificó y se adoptó en todas las organizaciones sociales y a todas las prácticas a las que se dedicaban los individuos, dando origen a grandes civilizaciones, donde cada una aportó diferentes instrumentos para la administración y tenencia de los recursos. Se podían concretar las negociaciones y luego la compensación, se podía entregar uno de los recursos en el momento y que la contraparte recibiera la contraprestación en otro momento, prácticas que debían evidenciarse por medio de registros.
          p.text-small(numero="5" titulo="Financiamiento") Civilizaciones como la de los sumerios y mesopotámicos realizaron grandes aportes a la contabilidad de los recursos, como la invención de sistemas de pesos y medidas para racionalizar el intercambio de cantidades de un recurso, la creación del pagaré como evidencia ante un intercambio pendiente de compensación, la letra de cambio que evidencia el acto de fe donde se declara la obligación de alguien de pagar o compensar por un bien recibido en un tiempo determinado, así como las prácticas de cobranza sobre esos derechos adquiridos por una transacción realizada en el pasado que tiene el registro y que está pendiente de cumplirse o ya se cumplió; todos los cuales constituyen esquemas iniciales de financiamiento tal como hoy se pueden realizar.
          p.text-small(numero="6" titulo="Sostenibilidad") En muchas otras civilizaciones del mundo, se adoptaron esquemas y sistemas similares en las organizaciones sociales, donde el papel predominante de la contabilidad fue sustantivo para el cumplimiento de los objetivos y la sostenibilidad de dichas organizaciones. 
          
