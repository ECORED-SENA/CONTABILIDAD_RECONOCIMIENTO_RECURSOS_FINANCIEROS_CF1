        LineaTiempoD.color-acento-contenido.t1-LineaTiempoD.mb-5
          p.text-small(numero="1" titulo="Ser asociativo") El hombre, como especie, tiene dentro de sus variadas características una en especial y es el ser asociativo, esto es, que necesita relacionarse con los otros individuos; y derivado de esta relación, se asocia con fines específicos, donde el primer objetivo es la supervivencia. Al inicio, el hombre, al igual que otras especies, era dependiente de la naturaleza y nómada, ya que no poseía la capacidad de transformar su entorno y renovar los recursos, lo que hacía que tuviera comportamientos de desplazamiento, máxime en zonas de influencia de las estaciones, que lo dejaban más vulnerable. Las primeras asociaciones derivadas de ese relacionamiento continuo fueron las familias, donde se establecieron roles de desempeño, lo que dio como resultado el origen del trabajo en función de los objetivos comunes.
