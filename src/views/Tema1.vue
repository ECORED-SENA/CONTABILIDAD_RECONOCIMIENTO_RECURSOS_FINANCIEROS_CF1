          p.text-small(numero="1" titulo="Imperio Romano") Durante el Imperio romano se aprobaron los registros o asientos en libros de contabilidad. Los banqueros de Roma fueron quienes perfeccionaron técnicas, con el fin de controlar las inversiones que hacían en el ejército para el logro de las conquistas de nuevos territorios.
          p.text-small(numero="2" titulo="Venecia") En Venecia, entre los siglos VII y VIII, la actividad contable se convirtió en un oficio al servicio de los feudos, quienes llevaban los libros. Asimismo, esto repercutió y llevó a que en las ciudades de Venecia, Florencia y Génova se desarrollaran actividades de lo que se denominó comercio formal, centros de industria y de bancos, lo que les permitió desarrollar técnicas de contabilidad eminentemente de servicios financieros de captación y colocación.
          p.text-small(numero="3" titulo="Padre contable") En Italia, hacia 1494, el monje franciscano Luca Pacioli propuso el concepto contable de la doble partida, consistente en que todo lo que se tiene proviene de algo, toda transacción tiene un efecto al débito y al crédito, registrando las transacciones de manera cronológica a su acontecimiento; concepto que a la fecha se sigue aplicando y por el cual se le denomina el “padre” de la contabilidad.
          p.text-small(numero="4" titulo="Estados Unidos") En Estados Unidos, hacia 1920, los bancos y las bolsas de valores comenzaron a exigir estados financieros certificados por contadores públicos independientes a quienes querían acceder a créditos y a realizar actividades bursátiles.
          p.text-small(numero="5" titulo="Latinoamérica") En nuestra latitud Latinoamérica, los diferentes países, a pesar de lograr su independencia, siguieron aplicando las técnicas contables españolas. En el caso de Colombia, solo en el año 1993, se reglamentó el sistema de contabilidad general, y se expidió, a partir del decreto 2649, el conjunto de normas y principios de la contabilidad general y aceptada -PCGA, nombre que surge de la traducción de las Normas Internacionales de Contabilidad-NIC. 
          p.text-small(numero="6" titulo="Colombia") Ya en tiempos recientes, se dicta la ley 2420 de 2015, donde se adoptaron, para Colombia, las Normas Internacionales de Información Financiera- NIIF, cuyo objetivo es dar uniformidad a la presentación de la información contable y financiera de las empresas u organizaciones, facilitando su entendimiento y sin tener diferencias según la nacionalidad de quienes las aplican o implementan en su empresa.      
