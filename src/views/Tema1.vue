      h2 1.5 Fundamentos contables
    p.mb-5 La contabilidad se sostiene sobre dos grandes columnas de idéntica y exacta magnitud económica, a las cuales se les denomina <b>Activo y Pasivo</b>. A través de la figura de “Posición financiera”, se debe establecer que la primera columna de <b>Activo</b> compila todos los recursos, bienes y derechos que posee la empresa u organización, con los que opera y a partir de los cuales toma decisiones para adecuarlos y desarrollar su actividad u objeto social, tal como pueden ser, por ejemplo: dinero en efectivo y el depositado en bancos, los inventarios, las edificaciones propias, locales propios, la maquinaria propia. La segunda columna, denominada <b>Pasivo</b>, compila el conjunto de recursos de procedencia propia y ajena, como, por ejemplo, las deudas con bancos, con proveedores y demás obligaciones que se tienen con terceros, esto es, la parte financiada de los recursos que posee la empresa, donde los fondos que configuran el patrimonio o aportes de los socios no son de exigibilidad u obligación.
    
